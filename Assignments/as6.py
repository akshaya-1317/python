#2. Print the given number in words using if-elif-else and loop. (eg.1234 => one two three four)

#words = ['zero','one','two','three','four','five','six','seven','eight','nine']


num = int(input("Enter a number: "))

for x in num:



  if x == 0:
    print("zero")
  elif x==1:
    print("One")
  elif x==2:
    print("Two")
  elif x==3:
    print("Three")
  elif x==4:
    print("Four")
  elif x==5:
    print("Five")
  elif x==6:
    print("Six")
  elif x==7:
    print("Seven")
  elif x==8:
    print("Eight")
  elif x==9:
    print("Nine")
  else:
      print("NO")








